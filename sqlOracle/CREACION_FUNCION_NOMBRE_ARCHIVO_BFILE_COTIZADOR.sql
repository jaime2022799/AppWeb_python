

CREATE OR REPLACE FUNCTION NOMBRE_ARCHIVO_BFILE_COTIZADOR (V_NOMBRE VARCHAR)
RETURN VARCHAR

IS 
V_VALIDACION VARCHAR2(100);


BEGIN

SELECT NOMBRE INTO V_VALIDACION
FROM ARCHIVO_BFILE_COTIZADOR
WHERE NOMBRE=V_NOMBRE;
RETURN V_VALIDACION;

EXCEPTION
    WHEN no_data_found then return 0;

END;

